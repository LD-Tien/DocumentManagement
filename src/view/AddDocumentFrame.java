    private void addDocButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDocButtonActionPerformed
        String docType = (String) docTypeComboBox.getSelectedItem();
        
        // thông báo lỗi khi trùng maTL
        ResultSet rs = documentManagement.getAllDocument();
        try {
            while (rs.next()) {
                if (maTLTextField.getText().equals(rs.getString("maTL"))) {
                    JOptionPane.showMessageDialog(this, "maTL đã tồn tại");
                    maTLTextField.requestFocus();
                    return;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddDocumentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // lỗi chưa nhập dữ liệu
        if (maTLTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập maTL!");
            maTLTextField.requestFocus();
            return;
        }      
        if (tenNXBTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tenNXB!");
            tenNXBTextField.requestFocus();
            return;
        }
        if (soBanPHTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập soBanPH!");
                soBanPHTextField.requestFocus();
                return;
            }
        
        if (docType.equals("Sách")) {
            // lỗi chưa nhập dữ liệu
            if (soTrangTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập soTrang!");
                soTrangTextField.requestFocus();
                return;
            }
            if (tenTGTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập tenTG!");
                tenTGTextField.requestFocus();
                return;
            }
            
            // set dữ liệu và insert
            book.setLoai(docType);
            book.setMaTaiLieu(maTLTextField.getText());
            book.setSoBanPhatHanh(Integer.parseInt(soBanPHTextField.getText()));
            book.setTenNXB(tenNXBTextField.getText());
            book.setTenTG(tenTGTextField.getText());
            book.setSoTrang(Integer.parseInt(soTrangTextField.getText()));
            documentManagement.insertBook(book);
        }
        if (docType.equals("Tạp chí")) {
            // lỗi chưa nhập dữ liệu
            if (soPHTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập soPH!");
                soPHTextField.requestFocus();
                return;
            }
            if (thangPHTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập thangPH!");
                thangPHTextField.requestFocus();
                return;
            }
            
            // set dữ liệu và insert
            journal.setLoai(docType);
            journal.setMaTaiLieu(maTLTextField.getText());
            journal.setSoBanPhatHanh(Integer.parseInt(soBanPHTextField.getText()));
            journal.setTenNXB(tenNXBTextField.getText());
            journal.setSoPhatHanh(Integer.parseInt(soPHTextField.getText()));
            journal.setThangPhatHanh(thangPHTextField.getText());
            documentManagement.insertJournal(journal);
        }
        if (docType.equals("Báo")){ 
            // lỗi chưa nhập dữ liệu
            if (ngayPHTextField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập ngayPH!");
                ngayPHTextField.requestFocus();
                return;
            }
            
            // set dữ liệu và insert
            newspaper.setLoai(docType);
            newspaper.setMaTaiLieu(maTLTextField.getText());
            newspaper.setSoBanPhatHanh(Integer.parseInt(soBanPHTextField.getText()));
            newspaper.setTenNXB(tenNXBTextField.getText());
            newspaper.setNgayPhatHanh(ngayPHTextField.getText());
            documentManagement.insertNewspaper(newspaper);
        }
        new ListDocumentFrame().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_addDocButtonActionPerformed


