    private void docTypeComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_docTypeComboBoxActionPerformed
        String docType  = (String) docTypeComboBox.getSelectedItem();
        
        // ẩn vùng không cho nhập khi chọn type
        tenTGTextField.setEnabled(false);
        soTrangTextField.setEnabled(false);
        soPHTextField.setEnabled(false);
        thangPHTextField.setEnabled(false);
        ngayPHTextField.setEnabled(false);
        if(docType.equals("Sách")) {
            tenTGTextField.setEnabled(true);
            soTrangTextField.setEnabled(true);
        }
        if(docType.equals("Tạp chí")) {
            soPHTextField.setEnabled(true);
            thangPHTextField.setEnabled(true);
        }
        if(docType.equals("Báo")) {
            ngayPHTextField.setEnabled(true);
        }
    }//GEN-LAST:event_docTypeComboBoxActionPerformed

